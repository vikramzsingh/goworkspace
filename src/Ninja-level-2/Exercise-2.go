//Using the following operators, write expressions and assign their values to variables:
//g.	==
//h.	<=
//i.	>=
//j.	!=
//k.	<
//l.	>
//Now print each of the variables.

package main

import "fmt"

func main() {
	g := (5 == 6)
	h := (5 <= 6)
	i := (5 >= 6)
	j := (5 != 6)
	k := (5 < 6)
	l := (5 > 6)

	fmt.Println(g)
	fmt.Println(h)
	fmt.Println(i)
	fmt.Println(j)
	fmt.Println(k)
	fmt.Println(l)

}
